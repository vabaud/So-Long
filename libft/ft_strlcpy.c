#include "libft.h"

size_t	ft_strlcpy(char *dest, const char *src, size_t size)
{
	size_t	i;
	size_t	taille;

	i = 0;
	taille = ft_strlen(src);
	if (!dest || !src)
		return (0);
	if (size == 0)
		return (taille);
	if (taille != 1)
	{
		while (src[i] != '\0' && i < (size - 1))
		{
			dest[i] = src[i];
			i++;
		}
		dest[i] = '\0';
	}
	return (taille);
}
